                                        <p>
                                        	我们中的许多人工作在无休止的任务，浏览器任务，社交媒体，电子邮件，会议，从一件事情到另一件事情，从不停顿，永无止境。然后这一天结束了，我们筋疲力尽，而且我们往往很少有人表现出来。我们从第二天开始，为无意识的任务和分心做好准备。

											我喜欢反对大多数人所做的事情，退后一步。是不是真的值得吗？这是最好的方法吗？我们是否因为忙碌和分心而失去生命？
											
											如果我们做得更少呢？当然，我一直建议在Zen Habits上做六年以上的事情，但这是一个值得重访的话题，因为  它是如此必要。今天我提供一个简短的指南，为那些愿意尝试的人做更少的事情。
											
											策划你的任务
											人如果你通常每天做20到30件事（例如），小型和大型的任务和会议，这很好 - 但这些事情是否重要？它们是否足以完成你生活中应有的切割？
											
											相反，把自己想象成一个收藏家。每一天都是有限的，我们的生活是有限的，我们可以尝试尽可能多地将这些容器塞进这些容器中，或者我们可以收集到这些有限的日子里值得体验的体验。
											
											如果你正在剔除最好的经验和任务，你会在今天包括什么？作为一个例子，我的一天经常包含我喜欢的这些东西：冥想，写作，喝咖啡和阅读，与妻子和孩子共度时光，锻炼身体，吃健康膳食，帮助别人，散步，阅读，喝一杯或两个红酒。不是每一天都有这些东西，而不是那种顺序，但这些都是我认为值得一天的事情。
											
											创造更少的一天
											你今天可以减少吗？你会怎么做？
											
											答案是肯定的，即使你已经开始执行一些小任务，你今天也可以做得更少。这是我的建议：
											
											深吸一口气。花一点时间在忙碌的一天停下来，然后呼吸。注意你的呼吸一分钟，然后注意你的身体，你如何坐着，你的下巴是否紧握，紧张的是什么。
											现在考虑你的一天。你今天做了什么？一天剩下的时间里有什么？有什么可以放手的吗？你能在你的任务之间创造空间吗？你可以剔除你的任务，摆脱非必要的，只留下最好的？
											学会了解必要的知识。需要时间来弄清楚哪些任务会对您的生活，职业，帮助他人和改变世界的能力产生最大的影响。但是今天就开始学习 - 选择你认为最重要的东西，清除一些空间，然后开始研究这些东西。品尝它们。
											减少分心。考虑进行数字清理 - 从社交媒体，新闻，娱乐/八卦网站，您经常花费在分散注意力上的地方休息一天或一周。当你感到有些分心时，你会做什么？冥想，锻炼，创造。
											呼吸。享受新的空间。享受少做的美。
